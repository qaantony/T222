//Static method creation and calling the same

package pack1;

public class Class4 {

	public static void launchBrowser() {
		System.out.println("opening  Browser");
		System.out.println("launch browser successful/not successful");
		System.out.println("Writing result into logfile with time stamp"+"\r\n");

	}

	public static void AccessApplication() { 
		System.out.println("Entering url in address bar and clicking on go button");
		System.out.println("Access Application successful/not successful");
		System.out.println("Writing result into logfile with time stamp"+"\r\n");

	}

	public static void userLogin() {
		System.out.println("Entering username, password and click on ok button");
		System.out.println("User login successful/not successful");
		System.out.println("Writing result into logfile with time stamp"+"\r\n");

	}

	public static void userLogout() {		
		System.out.println("Clicking on logout");
		System.out.println("User logout successful/not successful");
		System.out.println("Writing result into logfile with time stamp"+"\r\n");

	}

	public static void quitBrowser() {
		System.out.println("Closing the browser");
		System.out.println("Browser closed successful/not successful");
		System.out.println("Writing result into logfile with time stamp"+"\r\n");

	}

	public static void main(String[] args) {
		System.out.println("####Create and open logfile####"+"\r\n");
		
		launchBrowser();
		AccessApplication();
		userLogin();
		userLogout();
		quitBrowser();
		
		System.out.println("\r\n"+"###Close the log file###"+"\r\n");
		
	}

}
